    private void jButtonBuscarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarUsuarioActionPerformed
        mod.setPesquisa(jTextFieldBuscarUsuario.getText());
        ModeloUsuario usuario = control.buscaUsuario(mod);
        if (usuario == null){
            jTextFieldBuscarUsuario.setText("");
        }else{
            jTextFieldNomeUsuario.setText(usuario.getNome());
            jTextFieldLoginUsuario.setText(usuario.getLogin());
            jTextFieldIdUsuario.setText(String.valueOf(usuario.getIdUsuario()));
            jButtonAlterarUsuario.setEnabled(true);
            jButtonExcluirUsuario.setEnabled(true);
            jTextFieldBuscarUsuario.setText("");
        }
    }//GEN-LAST:event_jButtonBuscarUsuarioActionPerformed

