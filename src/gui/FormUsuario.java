    private void jButtonSalvarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarUsuarioActionPerformed
        mod.setPesquisa(jTextFieldLoginUsuario.getText());
        ModeloUsuario usuario = control.verificaLogin(mod);
        if (jTextFieldNomeUsuario.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha o Nome para continuar");
            jTextFieldNomeUsuario.requestFocus();
        }else if (jTextFieldLoginUsuario.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha o Usu√°rio para continuar");
            jTextFieldLoginUsuario.requestFocus();
        }else if (usuario != null){
            JOptionPane.showMessageDialog(null, "Usu√°rio n√£o dispon√≠vel");
            jTextFieldLoginUsuario.requestFocus();
        }else if (jPasswordSenha.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha a Senha para continuar");
            jPasswordSenha.requestFocus();
        }else if(jPasswordSenha.getText().equals(jPasswordFieldConfirmaSenha.getText())){
            if (flag == 1){
            mod.setNome(jTextFieldNomeUsuario.getText());
            mod.setLogin(jTextFieldLoginUsuario.getText());
            mod.setSenha(jPasswordSenha.getText());
            control.salvar(mod);
            jTextFieldNomeUsuario.setText("");
            jTextFieldLoginUsuario.setText("");
            jPasswordSenha.setText("");
            jPasswordFieldConfirmaSenha.setText("");
            jTextFieldNomeUsuario.setEnabled(false);
            jTextFieldLoginUsuario.setEnabled(false);
            jPasswordSenha.setEnabled(false);
            jPasswordFieldConfirmaSenha.setEnabled(false);
            jButtonSalvarUsuario.setEnabled(false);
            jButtonCancelarUsuario.setEnabled(false);
            jButtonAlterarUsuario.setEnabled(false);
            jButtonExcluirUsuario.setEnabled(false);
            jTextFieldBuscarUsuario.setEnabled(true);
            jButtonBuscarUsuario.setEnabled(true);
        }else{
            mod.setIdUsuario(Integer.parseInt(jTextFieldIdUsuario.getText()));
            mod.setNome(jTextFieldNomeUsuario.getText());
            mod.setLogin(jTextFieldLoginUsuario.getText());
            mod.setSenha(jPasswordSenha.getText());
            control.editar(mod);
            jTextFieldNomeUsuario.setText("");
            jTextFieldLoginUsuario.setText("");
            jPasswordSenha.setText("");
            jPasswordFieldConfirmaSenha.setText("");
            jTextFieldNomeUsuario.setEnabled(false);
            jTextFieldLoginUsuario.setEnabled(false);
            jPasswordSenha.setEnabled(false);
            jPasswordFieldConfirmaSenha.setEnabled(false);
            jButtonSalvarUsuario.setEnabled(false);
            jButtonCancelarUsuario.setEnabled(false);
            jButtonNovoUsuario.setEnabled(true);
            jTextFieldBuscarUsuario.setEnabled(true);
            jButtonBuscarUsuario.setEnabled(true);
        }
        }else{
            JOptionPane.showMessageDialog(null, "Senha e Confirmar senha devem ser iguais");
        }
    }//GEN-LAST:event_jButtonSalvarUsuarioActionPerformed

